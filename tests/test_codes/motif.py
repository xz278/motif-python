    ax.plot(list(range(24*7)), reg)

    # plot regularity 
    for i in range(1, 7):
        ax.plot([i*24,i*24], [0, 1], color='0.4', linewidth=0.2, linestyle='--')

    # set plot configurations
    ax.set_xticks([11+x*24 for x in range(7)])
    ax.set_xticklabels(['Mon', 'Tue', 'Wed', 'Thur', 'Fri', 'Sat', 'Sun'])
    ax.set_ylabel('R(t)')
